  " 
  Collider:
  
  X -> Z <- Y 
  
  "
library(rethinking)

n <- 3000
X <- rbern(n, 0.5)
Y <- rbern(n, 0.5)
Z <- rbern(n, ifelse(X+Y>0,0.9,0.2))

cor(X,Y)

cor(X[Z==0], Y[Z==0])
cor(X[Z==1], Y[Z==1])
